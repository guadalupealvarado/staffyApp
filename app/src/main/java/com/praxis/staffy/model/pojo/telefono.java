package com.praxis.staffy.model.pojo;

public class telefono {
    private int id;
    private int id_tp_telefono;
    private String numero;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getId_tp_telefono() {
        return id_tp_telefono;
    }

    public void setId_tp_telefono(int id_tp_telefono) {
        this.id_tp_telefono = id_tp_telefono;
    }

    public String getNumero() {
        return numero;
    }

    public void setNumero(String numero) {
        this.numero = numero;
    }
}
